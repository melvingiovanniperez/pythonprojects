# Sometimes the English name of a language begins like its code,
# like "Zulu" for "zu". Sometimes not at all, like "Spanish" for "es".
# This function should return how many letters match in the beginning,
# 0, 1 or 2 so for example nmatching('eo') should return 1.

def nmatching(code):
    name = langnames[code].lower()
    counter = 0
    if code[0] == name[0]:
        for i in range(2):
            if code[i] == name[i]:
                counter += 1
    else:
        return 0
    return counter 
            
# This function should report (by printing) which language codes
# don't match their English names at all. Let it print lines like
# "hy does not match Armenian", and have the output sorted according
# to language codes.

def report_no_match():
    for code in sorted(langnames):
        if nmatching(code) == 0:
            print(code, 'does not match', langnames[code])

# This should report language codes that could trick you (or at least me!)
# by looking like the beginning of some other language, but not as the one
# it's about. An example is "se" for "Northern Sami" where someone might think
# it stands for "Serbian" which begins with "Se-".
#
# That "sw" doesn't stand for "Swedish" doesn't count, because what it
# stands for is "Swahili" which also matches both letters. (So I might
# just as well believe that instead.)
#
# So more precisely we're looking for languages that have names for
# which the two first letters are valid language codes for languages for which 
# their names *don't* match both letters.
#
# Print lines similar to
#   "Note that Serbian is not 'se' since that means Northern Sami!"

# To get the values of langnames (that is all the English names)
# use  langnames.values()

def report_traps():
    for code in sorted(langnames):
            if nmatching(code) == 0: 
                for langname in langnames.values():
                    if langname[0:2].lower() == code[0:2]:
                        print('Note that', langname, 'is not', code, 'since that means', langnames[code],'!')
