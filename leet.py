def leet(string):
    new_sent = ''
    for char in string:
        if char.lower() in leettrans:
            if random.random() < .56:
                new_sent += random.choice(leettrans[char.lower()]) 
            else:
                new_sent+= char
        else:
            new_sent += char
    return new_sent
        
def leetloop():
    string = input("Let's Leet!\n")
    while string:
        print(leet(string))
        string = input("Let's Leet!\n")
